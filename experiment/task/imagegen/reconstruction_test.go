package imagegen

import (
	"fmt"
	"os"
	"path/filepath"
	"testing"

	. "github.com/smartystreets/goconvey/convey"
	"github.com/theapemachine/six/data"
	"github.com/theapemachine/six/experiment/projector"
	"github.com/theapemachine/six/provider/huggingface"
	"github.com/theapemachine/six/store"
	"github.com/theapemachine/six/tokenizer"
	"github.com/theapemachine/six/vm"
)

func TestReconstruction(t *testing.T) {
	Convey("Given a machine", t, func() {
		loader := vm.NewLoader(
			vm.LoaderWithStore(store.NewLSMSpatialIndex(1.0)),
			vm.LoaderWithTokenizer(tokenizer.NewUniversal(
				tokenizer.TokenizerWithDataset(
					huggingface.New(
						huggingface.DatasetWithRepo(
							"uoft-cs/cifar10",
						),
						huggingface.DatasetWithSamples(100),
						huggingface.DatasetWithTextColumn("img"),
						huggingface.DatasetWithTransform(huggingface.DecodeImageBytes),
						huggingface.DatasetWithPerSamplePos(),
					),
				),
			)),
		)
		
		machine := vm.NewMachine(
			vm.MachineWithLoader(loader),
		)

		machine.Start()
		loader.Holdout(50, vm.HoldoutRandom)
		
		Convey("When reconstructing masked out halves of images", func() {
			var stripRows []projector.ImageStripRow

			coder := tokenizer.NewMortonCoder()
			var buf []data.Chord

			for chord := range loader.Generate() {
				if chord.ActiveCount() == 0 {
					var tokenIDs []uint64

					for res := range machine.Prompt(buf) {
						tokenIDs = append(tokenIDs, loader.Lookup([]data.Chord{res.Chord[0]})...)
					}

					for _, tokenID := range tokenIDs {
						b, _, _ := coder.Decode(tokenID)
						fmt.Print(b)
					}

					fmt.Println()
					buf = buf[:0]
				}
				
				buf = append(buf, chord)
			}
			
			Convey("Artifacts should be written to the paper directory natively", func() {
				So(WriteImageStrip(stripRows, "Vision Pipeline Arbitrary Byte Reconstruction", "Image completions generated by topology routing directly over random holdout holes on CIFAR10.", "vision-reconstruction", "vision_cifar10_strip"), ShouldBeNil)
				_, statErr := os.Stat(filepath.Join(PaperDir(), "vision_cifar10_strip.tex"))
				So(statErr, ShouldBeNil)
			})
		})
	})	
}