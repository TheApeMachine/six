type stubPipelineMachine struct {
	startCalls  int
	promptCalls int
	outputs     [][]byte
	substrate   *geometry.HybridSubstrate
	startErr    error
}

func (machine *stubPipelineMachine) Start() error {
	machine.startCalls++
	if machine.substrate == nil {
		machine.substrate = geometry.NewHybridSubstrate()
	}

	return machine.startErr
}

func (machine *stubPipelineMachine) Prompt(_ []data.Chord, _ *geometry.IcosahedralManifold) chan byte {
	out := make(chan byte, 16)
	payloadIdx := machine.promptCalls
	machine.promptCalls++

	go func() {
		defer close(out)

		if payloadIdx >= len(machine.outputs) {
			return
		}

		for _, b := range machine.outputs[payloadIdx] {
			out <- b
		}
	}()

	return out
}

func (machine *stubPipelineMachine) Think(prompt []data.Chord, expected *geometry.IcosahedralManifold) chan byte {
	return machine.Prompt(prompt, expected)
}

func (machine *stubPipelineMachine) Substrate() *geometry.HybridSubstrate {
	if machine.substrate == nil {
		machine.substrate = geometry.NewHybridSubstrate()
	}

	return machine.substrate
}

type stubPipelineReporter struct {
	resultsCalls   int
	artifactCalls  int
	lastExperiment tools.PipelineExperiment
}

func (reporter *stubPipelineReporter) WriteResults(experiment tools.PipelineExperiment) error {
	reporter.resultsCalls++
	reporter.lastExperiment = experiment
	return nil
}

func (reporter *stubPipelineReporter) WriteArtifact(experiment tools.PipelineExperiment, _ tools.Artifact) error {
	reporter.artifactCalls++
	reporter.lastExperiment = experiment
	return nil
}

type stubPipelineExperiment struct {
	name      string
	section   string
	dataset   provider.Dataset
	prompts   *tokenizer.Prompt
	results   []tools.ExperimentalData
	artifacts []tools.Artifact
	finalized bool
	finalSub  *geometry.HybridSubstrate
	finalErr  error
}

func (experiment *stubPipelineExperiment) Name() string { return experiment.name }

func (experiment *stubPipelineExperiment) Section() string { return experiment.section }

func (experiment *stubPipelineExperiment) Dataset() provider.Dataset { return experiment.dataset }

func (experiment *stubPipelineExperiment) Prompts() *tokenizer.Prompt { return experiment.prompts }

func (experiment *stubPipelineExperiment) Holdout() (int, tokenizer.HoldoutType) {
	return 0, tokenizer.RIGHT
}

func (experiment *stubPipelineExperiment) AddResult(result tools.ExperimentalData) {
	experiment.results = append(experiment.results, result)
}

func (experiment *stubPipelineExperiment) Outcome() (any, gc.Assertion, any) {
	return len(experiment.results), gc.ShouldBeGreaterThanOrEqualTo, 0
}

func (experiment *stubPipelineExperiment) TableData() any { return experiment.results }

func (experiment *stubPipelineExperiment) Artifacts() []tools.Artifact { return experiment.artifacts }

func (experiment *stubPipelineExperiment) Finalize(substrate *geometry.HybridSubstrate) error {
	experiment.finalized = true
	experiment.finalSub = substrate
	return experiment.finalErr
}

func newTestPipeline(machine pipelineMachine, experiment tools.PipelineExperiment, reporter Reporter) *Pipeline {
	pipeline := &Pipeline{
		machine:    machine,
		experiment: experiment,
		reporter:   reporter,
		chordMap:   make(map[data.Chord]byte, 256),
		scoreWgts:  tools.DefaultScoreWeights(),
	}

	for b := range 256 {
		pipeline.chordMap[data.BaseChord(byte(b))] = byte(b)
	}

	return pipeline
}

func TestPipelineRunKeepsGeneratedBytes(t *testing.T) {
	t.Parallel()

	experiment := &stubPipelineExperiment{
		name:    "Generated Bytes",
		section: "unit",
		prompts: tokenizer.NewPrompt(
			tokenizer.PromptWithValues([]string{"abc"}),
			tokenizer.PromptWithHoldout(50, tokenizer.RIGHT),
		),
	}
	machine := &stubPipelineMachine{
		outputs:   [][]byte{[]byte("XYZ")},
		substrate: geometry.NewHybridSubstrate(),
	}
	reporter := &stubPipelineReporter{}

	pipeline := newTestPipeline(machine, experiment, reporter)

	require.NoError(t, pipeline.Run())
	require.Len(t, experiment.results, 1)
	require.Equal(t, []byte("ab"), experiment.results[0].Prefix)
	require.Equal(t, []byte("c"), experiment.results[0].Holdout)
	require.Equal(t, []byte("XYZ"), experiment.results[0].Observed)
	require.True(t, experiment.finalized)
	require.Equal(t, 1, machine.startCalls)
	require.Equal(t, 1, machine.promptCalls)
	require.Equal(t, 1, reporter.resultsCalls)
}

func TestPipelineRunAllowsAnalysisOnlyExperiments(t *testing.T) {
	t.Parallel()

	experiment := &stubPipelineExperiment{
		name:    "Analysis Only",
		section: "unit",
	}
	machine := &stubPipelineMachine{substrate: geometry.NewHybridSubstrate()}
	reporter := &stubPipelineReporter{}

	pipeline := newTestPipeline(machine, experiment, reporter)

	require.NoError(t, pipeline.Run())
	require.Empty(t, experiment.results)
	require.True(t, experiment.finalized)
	require.Equal(t, 1, machine.startCalls)
	require.Equal(t, 0, machine.promptCalls)
	require.Equal(t, 1, reporter.resultsCalls)
	require.Equal(t, 0, reporter.artifactCalls)
}

func TestPipelineRunSupportsScalingExperimentsWithoutDatasets(t *testing.T) {
	t.Parallel()

	experiment := scaling.NewBestFillScalingExperiment()
	reporter := &stubPipelineReporter{}

	pipeline, err := NewPipeline(
		PipelineWithExperiment(experiment),
		PipelineWithReporter(reporter),
	)
	require.NoError(t, err)
	require.NoError(t, pipeline.Run())
	require.Equal(t, 1, reporter.resultsCalls)
}

