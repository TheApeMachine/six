package cortex

import (
	"github.com/theapemachine/six/console"
	"github.com/theapemachine/six/data"
	"github.com/theapemachine/six/geometry"
)

/*
Think is the top-level cortex execution.

Lifecycle:
 1. BIRTH   — Prompt chords are injected into the source node.
 2. VIBRATE — The tick loop runs until convergence or timeout.
 3. EXTRACT — Output bytes are read from the sink via BestFace.
 4. REINJCT — Each extracted byte is re-injected into the source
    as the next prompt context (cascade auto-regression).
 5. DEATH   — The graph dissolves. The chan is closed.

The returned channel emits generated bytes as they are produced.
The caller should range over it.
*/
func (g *Graph) Think(prompt []data.Chord, expected *geometry.IcosahedralManifold) chan byte {
	out := make(chan byte, 1)

	go func() {
		defer close(out)

		// ── BIRTH: inject prompt ──────────────────────────────────
		g.InjectChords(prompt)

		// If we have an expected reality, seed it into the sink
		// as structural context (gives the graph a target attractor).
		if expected != nil {
			for i := range geometry.CubeFaces {
				if expected.Cubes[0][i].ActiveCount() > 0 {
					g.sink.Send(NewDataToken(expected.Cubes[0][i], -1))
				}
			}
		}

		// Let the initial injection diffuse before extracting output.
		// Run a "warmup" phase: tick the graph until either convergence
		// or a warmup budget is spent.
		warmup := g.config.MaxTicks / 4
		if warmup < 16 {
			warmup = 16
		}
		for range warmup {
			if g.Tick() {
				break
			}
		}

		console.Info("cortex warmup complete",
			"ticks", g.tick,
			"nodes", len(g.nodes),
			"sinkEnergy", g.sink.Energy(),
			"sourceEnergy", g.source.Energy(),
		)

		// ── EXTRACT: cascade auto-regressive generation ──────────
		// For each output byte:
		//   1. Read BestFace from the sink
		//   2. Emit the byte
		//   3. Re-inject the byte's BaseChord into the source
		//   4. Let the graph re-converge
		//   5. Repeat
		reConvergeTicks := g.config.MaxTicks / g.config.MaxOutput
		if reConvergeTicks < 8 {
			reConvergeTicks = 8
		}

		for i := range g.config.MaxOutput {
			face := g.sink.BestFace()

			// Face 256 = delimiter → sequence complete.
			if face == 256 {
				console.Info("cortex stop: delimiter",
					"bytesGenerated", i,
					"totalTicks", g.tick,
				)
				break
			}

			// No active face = exhausted.
			if g.sink.Cube[face].ActiveCount() == 0 {
				console.Info("cortex stop: exhausted",
					"bytesGenerated", i,
					"totalTicks", g.tick,
				)
				break
			}

			// Emit the byte.
			out <- byte(face)

			// Clear the emitted face to prevent echo-loop.
			g.sink.Cube[face] = data.Chord{}

			// ── REINJECT: cascade this output back as input ──────
			// The generated byte becomes context for the next prediction.
			// This forces the system to "experience" its own output.
			reinjection := data.BaseChord(byte(face))
			g.source.Send(NewDataToken(reinjection, -1))

			// Let the graph re-converge with the new context.
			for range reConvergeTicks {
				if g.Tick() {
					break
				}
			}
		}

		console.Info("cortex dissolved",
			"totalTicks", g.tick,
			"finalNodes", len(g.nodes),
			"survivorCount", len(g.Survivors(0.1)),
		)
	}()

	return out
}
