func TestMachineIntegration_StopTerminatesGeneration(t *testing.T) {
	corpus := buildCorpus()
	machine, _ := buildIntegrationMachine(corpus, nil)
	machine.Start()

	// Start generation then immediately stop.
	ch := machine.Prompt(promptToChords("def "), nil)
	machine.Stop()

	// Drain the channel — it should close promptly.
	out := collectBytes(ch)
	// We just verify it doesn't hang. Some output is expected (at least prompt).
	require.GreaterOrEqual(t, len(out), 1)
}

func TestApplyEventsToContextTracksHeaderRotation(t *testing.T) {
	ctx := &geometry.IcosahedralManifold{}
	events := []int{
		geometry.EventDensitySpike,
		geometry.EventPhaseInversion,
		geometry.EventDensityTrough,
		geometry.EventLowVarianceFlux,
	}

	generation.ApplyEventsToContext(ctx, events)

	expectedRot := uint8(0)
	for _, ev := range events {
		expectedRot = geometry.StateTransitionMatrix[expectedRot][ev]
	}

	require.Equal(t, expectedRot, ctx.Header.RotState())
}
